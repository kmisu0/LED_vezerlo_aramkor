/* Application states

  Summary:
    Application states enumeration

  Description:
    This enumeration defines the valid application states.  These states
    determine the behavior of the application at various times.
*/

typedef enum
{
	APP_STATE_INIT=0,
    APP_STATE_GET_CURRENT_APP_PARAMERES,
    APP_STATE_TCP_SERVER_LISTENING,
    APP_STATE_TCP_SERVER_REPLYING,
	APP_STATE_SERVICE_TASKS,            
} APP_STATES;


// *****************************************************************************
/* Application Data

  Summary:
    Holds application data

  Description:
    This structure holds the application's data.

  Remarks:
    Application strings and buffers are be defined outside this structure.
 */

typedef struct
{
    APP_STATES state;
} APP_DATA;

typedef struct
{
    uint16_t PWM[5];
    uint16_t LED[5];
    int16_t TEMP[5];
    uint16_t IC[5];
    uint16_t COMM;
} APP_PARAMETERS;

